export const loggerInfo = (msg: string): void => {
  console.log(`[INFO]: ${msg}`)
}

export const loggerSuccess = (msg: string): void => {
  console.log(`[SUCCESS]: ${msg}`)
}

export const loggerError = (msg: string): void => {
  console.log(`[ERROR]: ${msg}`)
}

export const loggerWarning = (msg: string): void => {
  console.log(`[WARNING]: ${msg}`)
}
